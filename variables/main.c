#include <stdio.h>
#include <stdlib.h>

int main()
{
    int mon_int = 14;
    printf("Mon int = %d \n", mon_int);


    char mon_char = 'A';
    printf("Mon char = %c \n", mon_char);


    float mon_float = 0.5;
    printf("Mon float = %f \n", mon_float);

    double mon_double = 14.6;
    printf("Mon double = %f \n", mon_double);

    return 0;
}
